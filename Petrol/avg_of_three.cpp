#include <bits/stdc++.h>
#define ll long long
using namespace std;

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(0);

    ll t;
    cin >> t;
    while (t--)
    {
        ll x;
        cin >> x;
        ll a = 3 * x - 3;
        ll b = 2;
        ll c = 1;
        cout << a << " " << b << " " << c << endl;
    }
    return 0;
}